package org.example.model;

public class Config {
    private String url;
    private int callsPerSecond;

    // Getters and Setters
    public String getUrl() { return url; }
    public void setUrl(String url) { this.url = url; }
    public int getCallsPerSecond() { return callsPerSecond; }
    public void setCallsPerSecond(int callsPerSecond) { this.callsPerSecond = callsPerSecond; }
}
